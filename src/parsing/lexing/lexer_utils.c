#include "../../../inc/ms.h"


int	ft_is_space(char c)
{
	if (c == '\t' || c == '\v' || c == '\r' \
	|| c == '\f' || c == ' ' || c == '\n')
		return (1);
	return (0);
}

int	is_special(char c)
{
	if (c == '<' || c == '>' \
	|| c == '|' || c == '$' \
	|| c == '\'' || c == '"')
		return (1);
	return (0);
}
